function hash(s) {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return Math.abs(h);
}

export function boostHtml(lang, slug, minWords) {
  const pool = lang === "es" ? ES : lang === "ru" ? RU : EN;
  const need = Math.max(0, minWords);
  if (need === 0) return "";
  const h = hash(`${slug}|${lang}`);
  const step = 2 + (h % 4);
  const out = [];
  let words = 0;
  for (let i = 0, idx = h % pool.length; words < need && i < 120; i++) {
    const sent = pool[idx % pool.length];
    idx += step;
    out.push(`<p>${sent}</p>`);
    words += sent.split(/\s+/).length;
  }
  const title =
    lang === "es"
      ? "Guía ampliada (contexto Le Bandit)"
      : lang === "ru"
        ? "Дополнительный контекст Le Bandit"
        : "Extended guide (Le Bandit context)";
  return `\n<section class="prose"><h2>${title}</h2>${out.join("\n")}</section>\n`;
}

export function wordCountHtml(html) {
  const t = html.replace(/<[^>]+>/g, " ");
  return t.trim().split(/\s+/).filter(Boolean).length;
}
